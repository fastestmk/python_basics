li = [1, 2, 3, 4, 5, 6, 7]
print(li)
print(li[2:5]) # printing from index 2 to 4
print(li[3:6:2]) # index 3 to 5 skipping 1 element
print(li[:2]) # index 0 to 1 index
print(li[2:]) # index 2 to n
print(li[::-1]) # reverse
print(li[5:1:-1]) # index 5 to 2
print(li[5:0:-1]) # index 5 to 1

